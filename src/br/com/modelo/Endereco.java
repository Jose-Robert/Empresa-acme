package br.com.modelo;

public class Endereco {
	private Long id;
	private String descricao;
	
public Endereco() {
	
}

public Long getId() {
	return id;
}

public void setId(Long id) {
	this.id = id;
}

public String getDescricao() {
	return descricao;
}

public void setDescricao(String descricao) {
	this.descricao = descricao;
}

}
